CREATE TABLE SUPPLIER (
    SUPPLIER_CODE VARCHAR(10) PRIMARY KEY COMMENT '공급업체 코드',
    SUPPLIER_NAME VARCHAR(50) NOT NULL COMMENT '공급업체명',
    PHONE_NUMBER VARCHAR(20) COMMENT '전화번호'
) COMMENT = '공급업체';

