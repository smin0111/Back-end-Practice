CREATE TABLE STOCK (
    STOCK_IN_ID INT AUTO_INCREMENT PRIMARY KEY COMMENT '입고 ID',
    PRODUCT_CODE INT NOT NULL COMMENT '상품번호',
    SUPPLIER_CODE VARCHAR(10) NOT NULL COMMENT '공급업체코드',
    INBOUND_DATE DATE NOT NULL COMMENT '입고일자',
    INBOUND_EA INT NOT NULL COMMENT '입고수량',
    CONSTRAINT fk_stockin_product FOREIGN KEY (PRODUCT_CODE) REFERENCES PRODUCT(PRODUCT_CODE),
    CONSTRAINT fk_stockin_supplier FOREIGN KEY (SUPPLIER_CODE) REFERENCES SUPPLIER(SUPPLIER_CODE)
) COMMENT = '입고';

